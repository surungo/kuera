	public function salve($eleitorbean) {
		$results = null;
		$pessoaResult = null;
		$con = null;
		$dsm = new DataSourceManager ();
		try {
			$con = $dsm->getConn (get_class($this));
			$objDAO = new EleitorDAO ( $con );
			$pessoaDAO = new PessoaDAO ( $con );
			$eleitorCategoriaGrupoBusiness = new EleitorCategoriaGrupoBusiness();
						
			if($eleitorbean->getpessoa() != null ){
				$pessoabean = $pessoaDAO->findByCPF( $eleitorbean->getpessoa()->getcpf() );
				$eleitorbean->getpessoa()->setid($pessoabean->getid());
			}
			
			
			if (Util::getIdObjeto($eleitorbean->getpessoa()) == null || Util::getIdObjeto($eleitorbean->getpessoa()) == 0) {
				$pessoaResult = $pessoaDAO->insert ( $eleitorbean->getpessoa() );
				$eleitorbean->setpessoa($pessoaResult->getresposta());
			} else {
				$pessoaResult = $pessoaDAO->update ( $eleitorbean->getpessoa() );
				$eleitorbean->setpessoa($pessoaResult->getresposta());
			}
			
			if ($eleitorbean->getid () == null || $eleitorbean->getid () == 0) {
				$results = $objDAO->insert ( $eleitorbean );
			} else {
				$results = $objDAO->update ( $eleitorbean );
			}
			if(count($eleitorbean->geteleitorCategoriaGrupo())>0)
				$eleitorCategoriaGrupoBusiness->salveEleitor($eleitorbean);
		} catch ( Exception $ex ) {
			// rollback transaction
			$con->rollback ();
			$dsm->close ( $con );
			throw new Exception ( $ex->getMessage () );
		}
		try {
			if ($con != null) {
				// commit transaction
				$con->commit ();
				$dsm->close ( $con );
			}
		} catch ( Exception $ex ) {
			throw new Exception ( $ex->getMessage () );
		}
		return $results;
	}
