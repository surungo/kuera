					$results = $objDAO->getreturnDataBaseBean ();
					$results->setmensagem ( FALHOU );
				} else {
					$results = $objDAO->insert ( $bean );
				}
			} else {
				$results = $objDAO->update ( $bean );
			}
		} catch ( Exception $ex ) {
			// rollback transaction
			$con->rollback ();
			$dsm->close ( $con );
			throw new Exception ( $ex->getMessage () );
