				$campeonatoPilotoBean = $this->findByCampeonatoPiloto ( $bean );
				Util::echobr ( $dbg, 'PilotoCampeonatoBusiness salve $campeonatoPilotoBean->getid()', $campeonatoPilotoBean->getid () );
				Util::echobr ( $dbg, 'PilotoCampeonatoBusiness salve $campeonatoPilotoBean->getpiloto()', $campeonatoPilotoBean->getpiloto () );
